                  <div class="contentH3">Building FUSE</div>
                      <p>FUSE stands for Frontline Universal System for Enterprise Experience. While the DRG was a style guide, FUSE is a complete design system, with tools for UX and development working on Frontine/Rebellion to reduce rework and achieve consistency. We took functionality from the original DRG and added new improvements, such as the ability to search for patterns by name or ID, copying HTML directly from the pattern page, seeing hex and spacing values alongside the base specs, and centralizing all pattern information in one singular source of truth. The initial concept was prototyped using Axure, and tested with other designers at T-mobile for feedback.</p>
