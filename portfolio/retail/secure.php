				  <div class="contentH3">Sister Patterns vs. Pattern Pairs</div>
                      <p>In order to standardize patterns across different organizations, the concept of sister patterns and pattern pairs was introduced. Sister patterns were patterns that shared the same functionality but were specific to an organization (i.e. Rebellion ONLY). They would have different pattern IDs. Pattern pairs were patterns that shared the same HTML code, but had different states, such as input fields or buttons. They would share the same pattern ID. The goal was to eventually phase out the existence of sister patterns in the future.</p>
